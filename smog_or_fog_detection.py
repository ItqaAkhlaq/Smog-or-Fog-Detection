def predict_image(img_path, model, model_name, use_efficientnet_preprocess=False):
    """Predict on single image and return result"""

    # Load image
    img = keras.preprocessing.image.load_img(img_path, target_size=(224, 224))
    img_array = keras.preprocessing.image.img_to_array(img)
    img_array = np.expand_dims(img_array, axis=0)

    # Preprocess
    if use_efficientnet_preprocess:
        from tensorflow.keras.applications.efficientnet import preprocess_input
        img_array = preprocess_input(img_array)
    else:
        img_array = img_array / 255.0

    # Predict
    prediction = model.predict(img_array, verbose=0)
    confidence = float(prediction[0][0])

    # Interpret
    if confidence > 0.5:
        predicted_class = "Foggy"
        confidence_pct = confidence * 100
    else:
        predicted_class = "Clear"
        confidence_pct = (1 - confidence) * 100

    return predicted_class, confidence_pct, img


print(f"  Training: {len(train)} images ({len(train)/len(df_raw)*100:.1f}%)")
print(f"  Validation: {len(val)} images ({len(val)/len(df_raw)*100:.1f}%)")
print(f"  Testing: {len(test)} images ({len(test)/len(df_raw)*100:.1f}%)")

# Resize and save images
print("\nPreprocessing images...")
for split_name, split_df in [('train', train), ('val', val), ('test', test)]:
    print(f"\nProcessing {split_name} set...")
    for idx, row in split_df.iterrows():
        try:
            # Read and resize image
            img = Image.open(row['path'])
            img = img.convert('RGB')  # Ensure RGB
            img_resized = img.resize((IMG_WIDTH, IMG_HEIGHT), Image.LANCZOS)

            # Save preprocessed image
            save_path = f"{PREPROCESSED_PATH}{split_name}/{row['class']}/{row['filename']}"
            img_resized.save(save_path)
        except Exception as e:
            print(f"Error processing {row['filename']}: {e}")
    print(f"✓ {split_name} set processed")

# Setup data augmentation for training
train_datagen = ImageDataGenerator(
    rescale=1./255,
    rotation_range=15,
    width_shift_range=0.1,
    height_shift_range=0.1,
    horizontal_flip=True,
    brightness_range=[0.8, 1.2],
    zoom_range=0.2,
    fill_mode='nearest'
)

val_test_datagen = ImageDataGenerator(rescale=1./255)

# Create data generators

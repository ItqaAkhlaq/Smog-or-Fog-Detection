def generate_saliency_map(img_path, model, class_name):
    """Generate saliency map showing important pixels"""

    # Load and preprocess
    img = keras.preprocessing.image.load_img(img_path, target_size=(224, 224))
    img_array = keras.preprocessing.image.img_to_array(img) / 255.0
    img_array = np.expand_dims(img_array, axis=0)

    # Convert to tensor
    img_tensor = tf.Variable(img_array, dtype=tf.float32)

    # Compute gradients with respect to input
    with tf.GradientTape() as tape:
        tape.watch(img_tensor)
        predictions = model(img_tensor)
        loss = predictions[0][0]  # Get prediction score

    # Get gradients
    gradients = tape.gradient(loss, img_tensor)

    # Compute saliency map (absolute value of gradients)
    saliency = tf.reduce_max(tf.abs(gradients), axis=-1)[0]
    saliency = saliency.numpy()

    # Normalize
    saliency = (saliency - saliency.min()) / (saliency.max() - saliency.min() + 1e-10)

    # Get prediction
    confidence = float(predictions[0][0])
    if confidence > 0.5:
        pred_class = "Foggy"
        conf_pct = confidence * 100
    else:
        pred_class = "Clear"
        conf_pct = (1 - confidence) * 100

    return saliency, pred_class, conf_pct, img


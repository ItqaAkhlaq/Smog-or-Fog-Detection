def visualize_conv_activations(img_path, model, layer_name):
    """
    Visualize convolutional layer activations
    This works without needing gradients!
    """

    # Load and preprocess
    img = keras.utils.load_img(img_path, target_size=(224, 224))
    img_array = keras.utils.img_to_array(img)
    img_array = np.expand_dims(img_array, axis=0) / 255.0

    # Create model that outputs conv layer
    layer_model = keras.Model(
        inputs=model.inputs,
        outputs=model.get_layer(layer_name).output
    )

    # Get activations
    activations = layer_model.predict(img_array, verbose=0)

    # Get prediction
    pred = model.predict(img_array, verbose=0)[0, 0]

    return activations[0], pred, img

def create_activation_heatmap(activations):
    """Create heatmap from layer activations"""

    # Average across channels
    heatmap = np.mean(activations, axis=2)

    # Normalize
    heatmap = np.maximum(heatmap, 0)
    heatmap = heatmap / (np.max(heatmap) + 1e-10)

    return heatmap


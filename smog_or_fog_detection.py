"""## 3. Define the prediction function"""

def predict_fog_smog(image: Image.Image) -> str:
    """
    Predicts whether an image contains fog or smog using the MobileNetV2 model.

    Args:
        image (PIL.Image.Image): The input image.

    Returns:
        str: A string indicating the prediction (e.g., 'Foggy (98.5%)', 'Clear (90.1%)').
    """
    if image is None:
        return "No image provided."

    # Resize and preprocess the image
    image = image.resize((IMG_WIDTH, IMG_HEIGHT))
    img_array = keras.preprocessing.image.img_to_array(image)
    img_array = np.expand_dims(img_array, axis=0)  # Add batch dimension
    img_array = img_array / 255.0  # Rescale pixels to 0-1

    # Make prediction
    prediction = mobilenet_model.predict(img_array, verbose=0)[0][0]

    # Interpret result
    class_labels = {0: 'Clear', 1: 'Foggy'}
    if prediction > 0.5:
        predicted_class = class_labels[1]
        confidence = prediction
    else:
        predicted_class = class_labels[0]
        confidence = 1 - prediction

    return f"{predicted_class} ({confidence*100:.2f}%) Discriminant Score: {prediction:.4f}"

